// Program 22: Program using if-else
#include <iostream>

using namespace std;
int main() {
    int num;

    cout << "Enter a number: ";
    cin >> num;

    // if-else statement
    if (num % 2 == 0) {
        cout << num << " is an Even number." << endl;
    } else {
        cout << num << " is an Odd number." << endl;
    }

    return 0;
}



