// Program 21: Program using simple if
#include <iostream>

using namespace std;
int main() {
    int age;

    cout << "Enter your age: ";
    cin >> age;

    // Simple if statement
    if (age >= 18) {
        cout << "You are eligible to vote." << endl;
    }

    cout << "Program execution completed." << endl;

    return 0;
}



